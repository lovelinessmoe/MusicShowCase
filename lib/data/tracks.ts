/**
 * 根据域名获取对应的曲目
 * @param hostname - 当前域名
 * @returns 匹配的曲目，如果没有匹配则返回默认曲目
 */
export function getTrackByDomain(hostname: string): Track {
  // 移除 www. 前缀进行标准化匹配
  const normalizedHostname = hostname.replace(/^www\./, '');
  
  // 查找匹配域名的曲目
  const domainTrack = tracks.find(track => {
    if (track.domain === 'default') return false;
    const normalizedTrackDomain = track.domain?.replace(/^www\./, '');
    return normalizedTrackDomain === normalizedHostname;
  });
  
  if (domainTrack) {
    return domainTrack;
  }
  
  // 如果没有匹配的域名，返回默认曲目
  const defaultTrack = tracks.find(track => track.domain === 'default');
  return defaultTrack || tracks[0];
}

/**
 * 根据 ID 获取指定曲目
 * @param id - 曲目 ID
 * @returns 匹配的曲目或 undefined
 */
export function getTrackById(id: string): Track | undefined {
  return tracks.find(track => track.id === id);
}

/**
 * 获取所有曲目
 * @returns 所有曲目的数组
 */
export function getAllTracks(): Track[] {
  return tracks;
}
